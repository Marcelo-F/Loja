package Estoque;

public class RoupaFeminina extends Produto {
	private String minisaia;
	private String saia;
	private String calcinha;
	private  String sutia; 
	private String sandalia;
	private String	rasterinha;
	
	
	public String getMinisaia() {
		return minisaia;
	}
	public void setMinisaia(String minisaia) {
		this.minisaia = minisaia;
	}
	public String getSaia() {
		return saia;
	}
	public void setSaia(String saia) {
		this.saia = saia;
	}
	
	public String getCalcinha() {
		return calcinha;
	}
	public void setCalcinha(String calcinha) {
		this.calcinha = calcinha;
	}
	public String getSutia() {
		return sutia;
	}
	public void setSutia(String sutia) {
		this.sutia = sutia;
	}
	public String getSandalia() {
		return sandalia;
	}
	public void setSandalia(String sandalia) {
		this.sandalia = sandalia;
	}
	public String getRasterinha() {
		return rasterinha;
	}
	public void setRasterinha(String rasterinha) {
		this.rasterinha = rasterinha;
	} 
	
	

}
